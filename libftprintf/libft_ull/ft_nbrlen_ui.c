#include "../ft_printf.h"

int				ft_nbrlen_ui(unsigned int n)
{
	int		i;

	i = 0;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
