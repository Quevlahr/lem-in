#include "../ft_printf.h"

void			ft_putnbr_l(long nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb <= 9)
		ft_putchar(nb + '0');
	else
	{
		ft_putnbr_l(nb / 10);
		ft_putnbr_l(nb % 10);
	}
}
